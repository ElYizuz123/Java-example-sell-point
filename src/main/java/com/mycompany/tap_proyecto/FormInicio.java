    private void cargarUsuarios() {
        try {
            BufferedReader br = new BufferedReader(new FileReader(archivoUsuarios));
            String lectura = null;
            while( (lectura = br.readLine()) != null ) {
                JSONObject jsonObject=(JSONObject)JSONValue.parse(lectura);
                Gson gson=new Gson();
                Usuario u=gson.fromJson(jsonObject.toJSONString(), Usuario.class);
                System.out.println("Usuario leido:" +u.getNombre());
                usuarios.add(u);
            }
            br.close();
        
        } catch (Exception e) {
            e.printStackTrace();
        }

    }
